#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*p;

	if (n == 0)
		return ;
	i = 0;
	p = (unsigned char *)s;
	while (i <= n - 1)
	{
		p[i] = 0;
		i++;
	}
}
